package app.ebr.controllers.auth.schema;

public class MeResponseEntity {

    private long id;
    private String email;

    public MeResponseEntity(long id, String email) {
        this.id = id;
        this.email = email;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEmail() {
        return email;
    }
}
